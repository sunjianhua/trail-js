/**
 * This class is a point implementation
 *
 * @class Point
 * @constructor
 * @param x {Integer} the x position of the point
 * @param y {Integer} the y position of the point
 */
TRAIL.Point = function(x, y)
{
    this.x = x;
    this.y = y;
};


// constructor
TRAIL.Point.prototype.constructor = TRAIL.Point;